#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	unsigned int	i;

	p = (unsigned char *)s;
	i = 0;
	while (n > 0)
	{
		p[i] = '\0';
		i++;
		n--;
	}
}
