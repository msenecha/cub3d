#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	size_t	len;
	char	*strtrim;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1) - 1;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end]))
		end--;
	len = end - start + 1;
	strtrim = (char *)malloc(len + 1);
	if (!strtrim)
		return (NULL);
	ft_memcpy(strtrim, s1 + start, len);
	strtrim[len] = '\0';
	return (strtrim);
}
